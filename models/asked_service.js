import mongoose from "mongoose";

const Asked_service = new mongoose.Schema(
  {
    fullname: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    tel: {
      type: String,
      required: true,
    },
    canton: {
      type: String,
      required: true,
    },
    city: {
      type: String,
      required: true,
    },
    date: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
    category: {
      type: String,
      required: true,
    },
    asked_service_user: {
      type: mongoose.Schema.Types.ObjectId,
      required: true,
      ref: "User",
    },
    offer: [
      {
        type: mongoose.Schema.Types.ObjectId,
        ref: "Given_Offer",
      },
    ],
  },
  {
    timestamps: true,
  }
);

export default mongoose.model("Asked_service", Asked_service);
